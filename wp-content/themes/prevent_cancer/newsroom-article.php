                  <h2>Lay Health Workers Encourage Latinas to Be Screened</h2>
                  <strong>April 23, 2015</strong> </div>
                <div class="writer-area"> <span class="writer-image"><img src="<?php echo bloginfo('template_directory')?>/images/writer.jpg"></span><span class="writer-name">by <strong>Maggie Klee</strong></span> </div>
                <p>Latinas face disparities in breast cancer education as well as access to screening and follow-up care. Compared to non-Hispanic whites and African Americans, Latinas 50 and older have a higher risk of breast cancer and are more likely to die from it. Some of the reasons for these differences include:</p>
                <p>lack of access to linguistically and culturally appropriate healthcare services and education
                  the myth that getting a mammogram causes cancer
                  a strong desire to take care of family before self and financial barriers</p>
                <p>Compared to non-Hispanic whites, Latinas have a lower screening rate and tend to be diagnosed at later stages, when treatment may be less successful. As National Minority Health Month comes to a close, we want to highlight our programs working to remove these barriers.</p>
                <p>Celebremos la Vida! is a program launched in 1994 which provides breast and cervical cancer education and screening to medically underserved Hispanic women over 40 years of age. Bilingual and bicultural coordinators set up a comforting environment that encourages women to return for yearly check-ups, take care of their health and promote the message of early detection to other women in their families and communities. If breast or cervical cancer is detected, the coordinator works with collaborating medical centers to ensure free or low cost medical follow-up and treatment.</p>
                <p>Campeonas contra el cancer de seno (Champions against Breast Cancer), is another culturally tailored educational outreach program, that successfully reaches Latinas with important messages about breast cancer screening and early detection. Professionals train Latinas to become Campeonas, which are health advocates and trusted sources of information within their communities. Campeonas encourage their family and friends to get timely screenings and teach them how to incorporate cancer prevention into their daily lives. Education is considered the best way to remove barriers to breast cancer screening for Latinas and when it comes from an influential community member, it is even more powerful.</p>
                <p>In December, new Campeonas attended a training at Catholic Charities, Spanish Catholic Center in Washington,D.C. Several Campeonas reported back that after just one conversation, their friends were ready to get screened for breast cancer.</p>
                <p>Another Campeonas friend said, "People don't talk about these things. Nobody talks about getting a mammogram. But now I know that even though I don't have insurance, I'm going to look for where to get it" (Translated from Spanish). She said that her Campeona taught her that cancer is easiest to treat in its early stages and that there are low-cost or free screenings available.</p>
